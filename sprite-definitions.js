function sprites(id, width, height, xCount, yCount) {
  const res = [];
  for (let i = 0; i < xCount; i++) {
    for (let j = 0; j < yCount; j++) {
      res.push({
        id: id + i + j * xCount,
        ...rect(i * width, j * height, width, height),
      });
    }
  }
  return res;
}

function entitySprites(id, width, height, xCount, yCount,
                       left, right, top, bottom, flipXY) {
  const res = [];
  for (let i = 0; i < xCount; i++) {
    for (let j = 0; j < yCount; j++) {
      res.push({
        id: id + (flipXY ? j * xCount + i : i * yCount + j),
        ...rect(i * width, j * height, width, height),
        left, right, top, bottom,
      });
    }
  }
  return res;
}

function halfBeltSprites(id, width, height,
                         yStart, xCount, xCut, yCut,
                         left, right, top, bottom) {
  const res = [];
  for (let i = 0; i < xCount; i++) {
    const xOff = xCut < 0 ? -xCut : 0;
    const yOff = yCut < 0 ? -yCut : 0;
    res.push({
      id: id + i,
      ...rect(i * width + xOff, yStart + yOff,
          width - Math.abs(xCut), height - Math.abs(yCut)),
      left: left - (xCut < 0 ? -xCut : 0),
      right: right - (xCut > 0 ? xCut : 0),
      top: top - (yCut < 0 ? -yCut : 0),
      bottom: bottom - (yCut > 0 ? yCut : 0),
    });
  }
  return res;
}

function itemSprites(id, iconId) {
  return [
    ...(id ? [{id, ...rect(64)}] : []),
    ...(iconId ? [{id: iconId, ...rect(0, 0, 64, 64)}] : []),
  ];
}

/*
  terrain sprites y coordinate:
  32: 0
  64: 64
  128: 160
*/
function terrainSprites(id, count) {
  const res = [];
  for (let i = 0; i < count; i++) {
    res.push({id: id + i, ...rect(i * 32)});
  }
  return res;
}

function waterSprites(id, count) {
  const res = [];
  for (let i = 0; i < count; i++) {
    res.push({id: id + i, ...rect(i * 32)});
  }
  return res;
}

function resourceSprites(id, variations, count) {
  const res = [];
  for (let i = 0; i < variations; i++) {
    for (let j = 0; j < count; j++) {
      res.push({
        id: id + i * count + j,
        x: 8 + i * 64,
        y: 8 + 64 * (count - 1) - j * 64,
        width: 48,
        height: 48,
      });
    }
  }
  return res;
}

function rect(x, y, width, height) {
  return {
      x: x ?? 0,
      y: y ?? 0,
      width: width ?? 32,
      height: height ?? 32,
    };
}
